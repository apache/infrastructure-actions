from verify_action_build.diff_node_modules import diff_node_modules


class TestDiffNodeModules:
    def test_identical_modules(self, tmp_path):
        orig = tmp_path / "original"
        rebuilt = tmp_path / "rebuilt"
        orig.mkdir()
        rebuilt.mkdir()

        (orig / "lodash").mkdir()
        (rebuilt / "lodash").mkdir()
        (orig / "lodash" / "index.js").write_text("module.exports = {}")
        (rebuilt / "lodash" / "index.js").write_text("module.exports = {}")

        result = diff_node_modules(
            orig, rebuilt, "test", "repo", "a" * 40,
        )
        assert result is True

    def test_different_file_content(self, tmp_path):
        orig = tmp_path / "original"
        rebuilt = tmp_path / "rebuilt"
        orig.mkdir()
        rebuilt.mkdir()

        (orig / "lodash").mkdir()
        (rebuilt / "lodash").mkdir()
        (orig / "lodash" / "index.js").write_text("original")
        (rebuilt / "lodash" / "index.js").write_text("different")

        result = diff_node_modules(
            orig, rebuilt, "test", "repo", "a" * 40,
        )
        assert result is False

    def test_extra_package_in_original(self, tmp_path):
        orig = tmp_path / "original"
        rebuilt = tmp_path / "rebuilt"
        orig.mkdir()
        rebuilt.mkdir()

        (orig / "lodash").mkdir()
        (orig / "lodash" / "index.js").write_text("code")
        (orig / "malicious-pkg").mkdir()
        (orig / "malicious-pkg" / "index.js").write_text("evil")
        (rebuilt / "lodash").mkdir()
        (rebuilt / "lodash" / "index.js").write_text("code")

        result = diff_node_modules(
            orig, rebuilt, "test", "repo", "a" * 40,
        )
        assert result is False

    def test_empty_directories(self, tmp_path):
        orig = tmp_path / "original"
        rebuilt = tmp_path / "rebuilt"
        orig.mkdir()
        rebuilt.mkdir()

        result = diff_node_modules(
            orig, rebuilt, "test", "repo", "a" * 40,
        )
        assert result is True

    def test_noisy_files_ignored(self, tmp_path):
        orig = tmp_path / "original"
        rebuilt = tmp_path / "rebuilt"
        orig.mkdir()
        rebuilt.mkdir()

        (orig / "lodash").mkdir()
        (rebuilt / "lodash").mkdir()
        (orig / "lodash" / "index.js").write_text("code")
        (rebuilt / "lodash" / "index.js").write_text("code")
        # .package-lock.json differs but should be ignored
        (orig / ".package-lock.json").write_text("{}")
        (rebuilt / ".package-lock.json").write_text('{"different": true}')

        result = diff_node_modules(
            orig, rebuilt, "test", "repo", "a" * 40,
        )
        assert result is True

    def test_package_json_install_fields_ignored(self, tmp_path):
        import json
        orig = tmp_path / "original"
        rebuilt = tmp_path / "rebuilt"
        orig.mkdir()
        rebuilt.mkdir()

        (orig / "lodash").mkdir()
        (rebuilt / "lodash").mkdir()

        orig_pkg = {"name": "lodash", "version": "4.0.0", "_resolved": "https://old"}
        rebuilt_pkg = {"name": "lodash", "version": "4.0.0", "_resolved": "https://new"}

        (orig / "lodash" / "package.json").write_text(json.dumps(orig_pkg))
        (rebuilt / "lodash" / "package.json").write_text(json.dumps(rebuilt_pkg))

        result = diff_node_modules(
            orig, rebuilt, "test", "repo", "a" * 40,
        )
        assert result is True
